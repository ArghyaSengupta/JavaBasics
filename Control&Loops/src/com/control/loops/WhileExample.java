package com.control.loops;

public class WhileExample {

	public static void main(String[] args) {

		int i=1;
		int j=10;
		while(i<=10)//conditional loop
		{
			System.out.println(i);
			i++;//incremental
		}
		
		while(j>=1)//conditional loop
		{
			
			System.out.println(j);
			j--;//decremental
		}
	}

}
