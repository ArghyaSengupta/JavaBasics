package com.control.loops;

public class ForEachExample {
	public static void main(String args[]) {
		
		//for each works for same type of data
		//all are numbers?:yes
		int a[]= {12,23,34,32,23};
		//arrays don't allow different data type
		//i:int
		//every number in a:int
		for(int i:a) {
	     System.out.println(i);
}
		
	}

}
