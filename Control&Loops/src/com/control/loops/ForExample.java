package com.control.loops;

public class ForExample {

	public static void main(String args[]) {
		//for loop: incremental
		//i=1,2,3,4.....10
		for(int i=1;i<=10;i++) {
			//same operation
			System.out.println(i);
		}
		//for loop:decremental
		//i=10,9,8,...1
		for(int i=10;i>=1;i--) {
			System.out.println(i);
		}
	}
}
